    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int opc=JOptionPane.showConfirmDialog(null, "¿Deseas ACTUALIZAR este cliente?", "ACTUALIZAR", JOptionPane.YES_NO_OPTION);
        if(opc==JOptionPane.YES_OPTION){
            BasededatosManager bd = new BasededatosManager();
            bd.ejecutar("UPDATE Cliente SET nombre='"+nombre.getText()+"', calle='"+calle.getText()+"',colonia='"+colonia.getText()+"',cp='"+cp.getText()+"',telefono='"+telefono.getText()+"',email='"+email.getText()+"',ciudad='"+ciudad.getText()+"',rfc='"+rfc.getText()+"',estado='"+estado.getSelectedItem()+"' WHERE idcliente='"+idcliente+"';",user,pass);
            dispose();
        }
        if(opc==JOptionPane.NO_OPTION){
            dispose();
        }
    }//GEN-LAST:event_jButton3ActionPerformed


