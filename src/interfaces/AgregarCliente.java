        if(opc==JOptionPane.YES_OPTION){
            BasededatosManager bd = new BasededatosManager();
            bd.ejecutar("INSERT INTO Cliente (nombre, calle, colonia, cp, telefono, email, ciudad, rfc, estado) VALUES('"+nombre.getText()+"','"+calle.getText()+"','"+colonia.getText()+"','"+cp.getText()+"','"+telefono.getText()+"','"+email.getText()+"','"+ciudad.getText()+"','"+rfc.getText()+"','"+estado.getSelectedItem()+"');",user,pass);
            dispose();
        }
        if(opc==JOptionPane.NO_OPTION){
            dispose();
        }
