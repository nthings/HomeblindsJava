    public AgregarCliente(String id, String usuario, String password){
        try {
            this.user=usuario;
            this.pass=password;
            this.idcliente=id;
            initComponents();
            this.setClosable(true);
            BasededatosManager bd=new BasededatosManager();
            setTitle("Editar Cliente");
            jButton1.setVisible(false);
            jButton3.setVisible(true);
            jButton1.setOpaque(false);
            jButton1.setContentAreaFilled(false);
            jButton1.setBorderPainted(false);
            jButton2.setOpaque(false);
            jButton2.setContentAreaFilled(false);
            jButton2.setBorderPainted(false);
            jButton3.setOpaque(false);
            jButton3.setContentAreaFilled(false);
            jButton3.setBorderPainted(false);
            ResultSet consulta=bd.consultar("SELECT * FROM Cliente WHERE idcliente='"+idcliente+"';",user,pass);
            while(consulta.next()){
                nombre.setText(consulta.getString("nombre"));
                calle.setText(consulta.getString("calle"));
                colonia.setText(consulta.getString("colonia"));
                cp.setText(consulta.getString("cp"));
                telefono.setText(consulta.getString("telefono"));
                email.setText(consulta.getString("email"));
                ciudad.setText(consulta.getString("ciudad"));
                rfc.setText(consulta.getString("rfc"));
                estado.setSelectedItem(consulta.getObject("estado"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(AgregarCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

